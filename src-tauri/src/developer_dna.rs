/// Generate a complete Developer DNA snapshot
pub fn generate_dna() -> Result<DeveloperDna, String> {
    let conn = crate::open_db_connection()?;

    info!(target: "4da::dna", "Generating Developer DNA snapshot");

    // 1. Domain profile
    let profile = domain_profile::build_domain_profile(&conn);

    let mut primary_stack: Vec<String> = profile.primary_stack.into_iter().collect();
    primary_stack.sort();

    let mut adjacent_tech: Vec<String> = profile.adjacent_tech.into_iter().collect();
    adjacent_tech.sort();

    let mut interests: Vec<String> = profile.interest_topics.into_iter().collect();
    interests.sort();

    // 2. Top dependencies (from project_dependencies, limit 30)
    let top_dependencies = get_top_dependencies(&conn)?;

    // 3. Engagement data (from feedback/interactions)
    let top_engaged_topics = get_top_engaged_topics(&conn)?;

    // 4. Blind spots (from knowledge gaps)
    let blind_spots = get_blind_spots(&conn)?;

    // 5. Source engagement stats
    let source_engagement = get_source_engagement(&conn)?;

    // 6. Overall stats
    let stats = compute_stats(&conn)?;

    // 7. Identity summary
    let identity_summary = build_identity_summary(&primary_stack, &top_dependencies);

    Ok(DeveloperDna {
        generated_at: chrono::Utc::now().to_rfc3339(),
        primary_stack,
        adjacent_tech,
        top_dependencies,
        interests,
        top_engaged_topics,
        blind_spots,
        source_engagement,
        stats,
        identity_summary,
    })
}

/// Export Developer DNA as shareable markdown
pub fn export_as_markdown(dna: &DeveloperDna) -> String {
    let mut md = String::new();

    md.push_str("# Developer DNA\n\n");
    md.push_str(&format!("**{}**\n\n", dna.identity_summary));
    md.push_str(&format!(
        "*Generated by [4DA](https://github.com/4da-dev/4da-home) on {}*\n\n",
        &dna.generated_at[..10]
    ));

    // Primary Stack
    if !dna.primary_stack.is_empty() {
        md.push_str("## Primary Stack\n\n");
        for tech in &dna.primary_stack {
            md.push_str(&format!("- {}\n", capitalize(tech)));
        }
        md.push('\n');
    }

    // Top Dependencies
    if !dna.top_dependencies.is_empty() {
        md.push_str("## Key Dependencies\n\n");
        for dep in dna.top_dependencies.iter().take(15) {
            md.push_str(&format!("- `{}`\n", dep.name));
        }
        md.push('\n');
    }

    // Interests
    if !dna.interests.is_empty() {
        md.push_str("## Interests\n\n");
        for topic in &dna.interests {
            md.push_str(&format!("- {}\n", capitalize(topic)));
        }
        md.push('\n');
    }

    // Top Engaged Topics
    if !dna.top_engaged_topics.is_empty() {
        md.push_str("## Where My Attention Goes\n\n");
        md.push_str("| Topic | Engagement |\n|-------|------------|\n");
        for topic in dna.top_engaged_topics.iter().take(10) {
            let bar = bar_chart(topic.percent_of_total);
            md.push_str(&format!(
                "| {} | {} {:.0}% |\n",
                capitalize(&topic.topic),
                bar,
                topic.percent_of_total
            ));
        }
        md.push('\n');
    }

    // Blind Spots
    if !dna.blind_spots.is_empty() {
        md.push_str("## Blind Spots\n\n");
        for spot in dna.blind_spots.iter().take(5) {
            md.push_str(&format!(
                "- **{}** — {} severity, {}d stale\n",
                spot.dependency, spot.severity, spot.days_stale
            ));
        }
        md.push('\n');
    }

    // Stats
    md.push_str("## Stats\n\n");
    md.push_str(&format!(
        "- Items processed: {}\n",
        dna.stats.total_items_processed
    ));
    md.push_str(&format!(
        "- Rejection rate: {:.1}%\n",
        dna.stats.rejection_rate
    ));
    md.push_str(&format!(
        "- Projects monitored: {}\n",
        dna.stats.project_count
    ));
    md.push_str(&format!(
        "- Dependencies tracked: {}\n",
        dna.stats.dependency_count
    ));
    md.push_str(&format!("- Days active: {}\n", dna.stats.days_active));

    md
}

