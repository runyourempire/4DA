fn get_top_dependencies(conn: &rusqlite::Connection) -> Result<Vec<DependencyEntry>, String> {
    let mut deps = Vec::new();
    let mut stmt = conn
        .prepare(
            "SELECT DISTINCT package_name, project_path FROM project_dependencies
             WHERE is_dev = 0
             ORDER BY package_name
             LIMIT 50",
        )
        .map_err(|e| format!("Failed to query dependencies: {}", e))?;

    let rows = stmt
        .query_map([], |row| {
            Ok(DependencyEntry {
                name: row.get(0)?,
                project_path: row.get(1)?,
            })
        })
        .map_err(|e| format!("Failed to read dependencies: {}", e))?;

    for row in rows.flatten() {
        deps.push(row);
    }
    Ok(deps)
}

fn get_top_engaged_topics(conn: &rusqlite::Connection) -> Result<Vec<EngagedTopic>, String> {
    // Pull from topic_affinities (learned behavior)
    let mut topics = Vec::new();

    let query =
        "SELECT topic, affinity_score FROM topic_affinities ORDER BY affinity_score DESC LIMIT 20";
    if let Ok(mut stmt) = conn.prepare(query) {
        if let Ok(rows) = stmt.query_map([], |row| {
            Ok((row.get::<_, String>(0)?, row.get::<_, f64>(1)?))
        }) {
            let all: Vec<(String, f64)> = rows.flatten().collect();
            let total_weight: f64 = all.iter().map(|(_, w)| w).sum();

            for (topic, weight) in all {
                let pct = if total_weight > 0.0 {
                    (weight / total_weight * 100.0) as f32
                } else {
                    0.0
                };
                topics.push(EngagedTopic {
                    topic,
                    interactions: weight as u32,
                    percent_of_total: pct,
                });
            }
        }
    }

    Ok(topics)
}

fn get_blind_spots(conn: &rusqlite::Connection) -> Result<Vec<BlindSpotEntry>, String> {
    // Simplified: find dependencies we haven't engaged with recently
    let mut spots = Vec::new();

    let query = "
        SELECT pd.package_name, MAX(pd.project_path) as project_path
        FROM project_dependencies pd
        WHERE pd.is_dev = 0
        AND pd.package_name NOT IN (
            SELECT DISTINCT topic FROM topic_affinities WHERE weight > 0.3
        )
        AND LENGTH(pd.package_name) >= 4
        GROUP BY pd.package_name
        ORDER BY pd.package_name
        LIMIT 10
    ";

    if let Ok(mut stmt) = conn.prepare(query) {
        if let Ok(rows) = stmt.query_map([], |row| {
            Ok(BlindSpotEntry {
                dependency: row.get(0)?,
                severity: "medium".to_string(),
                days_stale: 30, // Approximate; would need temporal tracking for precision
            })
        }) {
            for row in rows.flatten() {
                spots.push(row);
            }
        }
    }

    Ok(spots)
}

fn get_source_engagement(conn: &rusqlite::Connection) -> Result<Vec<SourceEngagement>, String> {
    let mut sources = Vec::new();

    // Total items by source
    let query = "
        SELECT source_type, COUNT(*) as total,
               SUM(CASE WHEN id IN (SELECT item_id FROM feedback WHERE action = 'save') THEN 1 ELSE 0 END) as saved
        FROM source_items
        GROUP BY source_type
        ORDER BY total DESC
    ";

    if let Ok(mut stmt) = conn.prepare(query) {
        if let Ok(rows) = stmt.query_map([], |row| {
            let total: u32 = row.get(1)?;
            let saved: u32 = row.get(2)?;
            let rate = if total > 0 {
                saved as f32 / total as f32 * 100.0
            } else {
                0.0
            };
            Ok(SourceEngagement {
                source_type: row.get(0)?,
                items_seen: total,
                items_saved: saved,
                engagement_rate: rate,
            })
        }) {
            for row in rows.flatten() {
                sources.push(row);
            }
        }
    }

    Ok(sources)
}

fn compute_stats(conn: &rusqlite::Connection) -> Result<DnaStats, String> {
    let total_items: u32 = conn
        .query_row("SELECT COUNT(*) FROM source_items", [], |row| row.get(0))
        .unwrap_or(0);

    let total_relevant: u32 = conn
        .query_row(
            "SELECT COUNT(*) FROM source_items WHERE id IN (SELECT item_id FROM feedback WHERE action IN ('save', 'click'))",
            [],
            |row| row.get(0),
        )
        .unwrap_or(0);

    let rejection_rate = if total_items > 0 {
        (1.0 - total_relevant as f32 / total_items as f32) * 100.0
    } else {
        0.0
    };

    let project_count: u32 = conn
        .query_row(
            "SELECT COUNT(DISTINCT project_path) FROM project_dependencies",
            [],
            |row| row.get(0),
        )
        .unwrap_or(0);

    let dependency_count: u32 = conn
        .query_row(
            "SELECT COUNT(DISTINCT package_name) FROM project_dependencies WHERE is_dev = 0",
            [],
            |row| row.get(0),
        )
        .unwrap_or(0);

    // Days active: difference between earliest and latest source_items
    let days_active: u32 = conn
        .query_row(
            "SELECT CAST((julianday('now') - julianday(MIN(created_at))) AS INTEGER) FROM source_items",
            [],
            |row| row.get(0),
        )
        .unwrap_or(0);

    Ok(DnaStats {
        total_items_processed: total_items,
        total_relevant,
        rejection_rate,
        project_count,
        dependency_count,
        days_active,
    })
}

fn build_identity_summary(primary_stack: &[String], deps: &[DependencyEntry]) -> String {
    if primary_stack.is_empty() {
        return "Developer (no stack configured yet)".to_string();
    }

    let top_langs: Vec<String> = primary_stack
        .iter()
        .take(3)
        .map(|s| capitalize(s))
        .collect();

    // Try to infer domain from dependencies
    let domain = infer_domain(primary_stack, deps);

    format!("{} {} developer", top_langs.join("/"), domain)
}

fn infer_domain(stack: &[String], deps: &[DependencyEntry]) -> &'static str {
    let all: Vec<String> = stack
        .iter()
        .chain(deps.iter().map(|d| &d.name))
        .map(|s| s.to_lowercase())
        .collect();

    let has = |term: &str| all.iter().any(|s| s.contains(term));

    if has("tauri") || has("electron") || has("wry") {
        "desktop"
    } else if has("react-native") || has("flutter") || has("swift") || has("kotlin") {
        "mobile"
    } else if has("tensorflow") || has("pytorch") || has("scikit") || has("transformers") {
        "ML/AI"
    } else if has("kubernetes") || has("terraform") || has("docker") || has("ansible") {
        "infrastructure"
    } else if has("next") || has("nuxt") || has("svelte") || has("remix") {
        "fullstack"
    } else if has("react") || has("vue") || has("angular") {
        "frontend"
    } else if has("actix") || has("axum") || has("express") || has("fastapi") || has("django") {
        "backend"
    } else if has("embedded") || has("stm32") || has("esp32") {
        "embedded"
    } else if has("wasm") || has("webassembly") {
        "WebAssembly"
    } else {
        "systems"
    }
}

fn capitalize(s: &str) -> String {
    let mut chars = s.chars();
    match chars.next() {
        None => String::new(),
        Some(c) => c.to_uppercase().to_string() + chars.as_str(),
    }
}

fn bar_chart(percent: f32) -> String {
    let filled = (percent / 10.0).round() as usize;
    let empty = 10usize.saturating_sub(filled);
    format!("{}{}", "\u{2588}".repeat(filled), "\u{2591}".repeat(empty))
}

