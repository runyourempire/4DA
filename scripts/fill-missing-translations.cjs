// ─── MAIN LOGIC ──────────────────────────────────────────────────────────────

function sortKeys(obj) {
  const sorted = {};
  for (const key of Object.keys(obj).sort()) {
    sorted[key] = obj[key];
  }
  return sorted;
}

let totalAdded = 0;

for (const locale of LOCALES) {
  // ── ui.json ─────────────────────────────────────────────────────────
  const uiPath = path.join(LOCALES_DIR, locale, 'ui.json');
  const uiData = JSON.parse(fs.readFileSync(uiPath, 'utf8'));
  let uiAdded = 0;

  for (const [key, translations] of Object.entries(uiTranslations)) {
    if (!uiData[key] && translations[locale]) {
      uiData[key] = translations[locale];
      uiAdded++;
    }
  }

  if (uiAdded > 0) {
    const sorted = sortKeys(uiData);
    fs.writeFileSync(uiPath, JSON.stringify(sorted, null, 2) + '\n', 'utf8');
    console.log(`  ${locale}/ui.json: +${uiAdded} keys`);
    totalAdded += uiAdded;
  } else {
    console.log(`  ${locale}/ui.json: no changes needed`);
  }

  // ── errors.json ─────────────────────────────────────────────────────
  const errPath = path.join(LOCALES_DIR, locale, 'errors.json');
  const errData = JSON.parse(fs.readFileSync(errPath, 'utf8'));
  let errAdded = 0;

  for (const [key, translations] of Object.entries(errorsTranslations)) {
    if (!errData[key] && translations[locale]) {
      errData[key] = translations[locale];
      errAdded++;
    }
  }

  if (errAdded > 0) {
    const sorted = sortKeys(errData);
    fs.writeFileSync(errPath, JSON.stringify(sorted, null, 2) + '\n', 'utf8');
    console.log(`  ${locale}/errors.json: +${errAdded} keys`);
    totalAdded += errAdded;
  } else {
    console.log(`  ${locale}/errors.json: no changes needed`);
  }
}

console.log(`\nDone. Total keys added: ${totalAdded}`);
