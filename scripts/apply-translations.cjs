// Process each locale
const locales = ['es', 'fr', 'de', 'it', 'pt-BR', 'ja', 'ko', 'zh', 'ru', 'ar', 'hi', 'tr'];

let totalAdded = 0;

for (const locale of locales) {
  const filePath = path.join(LOCALES_DIR, locale, 'ui.json');

  // Read existing file
  const content = fs.readFileSync(filePath, 'utf8');
  const existing = JSON.parse(content);

  const newKeys = translations[locale];
  if (!newKeys) {
    console.error(`No translations for locale: ${locale}`);
    continue;
  }

  // Count how many we're adding
  let added = 0;
  for (const [key, value] of Object.entries(newKeys)) {
    if (!(key in existing)) {
      existing[key] = value;
      added++;
    }
  }

  // Sort keys alphabetically
  const sorted = {};
  for (const key of Object.keys(existing).sort()) {
    sorted[key] = existing[key];
  }

  // Write back
  const output = JSON.stringify(sorted, null, 2) + '\n';
  fs.writeFileSync(filePath, output, 'utf8');

  // Verify it's valid JSON
  try {
    JSON.parse(output);
    console.log(`${locale}: +${added} keys added (total: ${Object.keys(sorted).length})`);
    totalAdded += added;
  } catch (e) {
    console.error(`${locale}: INVALID JSON after write!`, e.message);
  }
}

console.log(`\nTotal keys added across all locales: ${totalAdded}`);
